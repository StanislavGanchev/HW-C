#include<stdio.h>

int main(void){
    
    char a = 250;    
    printf("%d\n", a);

    unsigned char b = 250;
    printf("%u\n", b); //%u = integers

    unsigned short c = 234;
    printf("%hu\n", c);

    unsigned long d = 233;
    printf("%lu\n", d);

    unsigned long long e = 222;
    printf("%llu\n", e);


    return 0;
}